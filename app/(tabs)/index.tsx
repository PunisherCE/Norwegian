function TopicButtons() {
  type wordsTo = { word: string, index: number }
  const [topic, setTopic] = useState<subjectType>('begining');
  const [words, setWord] = useState<wordsTo>({ word: 'Select a Category', index: -1 })
  const [translate, setTranslate] = useState<string>('Waiting...')
  const [disabler, setDisabler] = useState<boolean>(false)
  const [show, setShow] = useState<boolean>(true)
  const [reviewState, setReviewState] = useState<string>('')
  const [lista2, setLista2] = useState<string[]>([])

  async function addReview(item: string) {
    reviewList = reviewList? reviewList.concat(',', item): item
    await AsyncStorage.setItem('review', reviewList)
    setReviewState(reviewList)
  }
  async function removeReview(item: string) {
    if(reviewList.endsWith(item)){
      reviewList = reviewList.replace(',' + item , '')
    } else if((reviewList.endsWith(item) && reviewList.startsWith(item)) || (reviewList.endsWith(item + ',') && reviewList.startsWith(item))){
      reviewList = ''
    } else {
      reviewList = reviewList.replace(item + ',' , '')
    }
    await AsyncStorage.setItem('review', reviewList)
    setReviewState(reviewList)
  }

  return (
    <View style={styles.container}>
      <View style={{...styles.horizontalContainer, backgroundColor: '#000'}}>
        <FlatList
          contentContainerStyle={styles.listContainer}
          data={arrayTopics}
          renderItem={({ item }) => (
            <Pressable onPress={() => {
              if (topic !== item.topic) {
                setTopic(item.topic)
                setWord(getWords(item.topic))
                wordSet.clear()
                setLista2([])
                setShow(true)
                setTranslate('Waiting...')
                setDisabler(false)
              }
            }}
              style={topic === item.topic ? styles.pressable : styles.pressable2}>
              <Text style={styles.text}>
                {item.topic}
              </Text>
            </Pressable>
          )}
          keyExtractor={item => item.id.toString()}
          horizontal={true}
        />
      </View>
      <View style={{...styles.contentContainer, backgroundColor: '#000'}}>
        <View style={{ flexDirection: 'column', backgroundColor: '#000' }}>
          <Text 
          style={[styles.display, words.index % 2 === 0? {color: 'blueviolet'}: {color: 'blue'}]}
          selectable={true}>
            {words.word}
          </Text>
          <Text 
          style={styles.translated}
          selectable={true}>
            {translate}
          </Text>
        </View>
        <View style={styles.show}>
          <Pressable style={[styles.showButton, disabler? {backgroundColor: 'green'} : {backgroundColor: 'blue'}]}
            disabled={disabler}
            android_ripple={{color: 'purple', borderless: false}}
            onPress={() => {
              if (topic !== 'begining') {
                setTranslate(showTranslated(topic, words.index));
                setDisabler(true)
              }
            }}>
              <Text>Show!</Text>
          </Pressable>
        </View>
      </View>
      <View style={{...styles.answears, backgroundColor: '#000'}}>
        <View style={{backgroundColor: '#000'}}>
          <Pressable style={[styles.button, disabler? {backgroundColor: 'blue'} : {backgroundColor: 'green'}]}
            android_ripple={{color: 'red', borderless: false}}
            disabled={!disabler}
            onPress={() => {
              if (topic !== 'begining') {
                setDisabler(false)
                setShow(false)
                if (words.index % 2 === 0) {
                  if (!wordSet.has(words.index + 1)) {
                    wordSet.add(words.index)
                  }
                } else if (!wordSet.has(words.index - 1)) {
                  wordSet.add(words.index)
                }
                setWord(getWords(topic))
                setTranslate('Waiting...')
              }
            }}>
            <Text>Wrong?</Text>
          </Pressable>
        </View>       
        <View style={{backgroundColor: '#000'}}>
          <Pressable style={[styles.button, disabler? {backgroundColor: 'blue'} : {backgroundColor: 'green'}]}
            android_ripple={{color: 'purple', borderless: false}}
            disabled={!disabler}
            onPress={() => {
              if (topic !== 'begining') {
                setWord(getWords(topic))
                setTranslate('Waiting...')
                setDisabler(false)
              }
            }}>
            <Text>Right?</Text>
          </Pressable>
        </View>
      </View>
      <View style={{backgroundColor: '#000', marginBottom: 12}}>
        <Pressable style={[styles.button, show? {backgroundColor: 'green'} : {backgroundColor: 'blue'}]}
          android_ripple={{color: 'gray', borderless: true}} 
          disabled={show}
          onPress={() => {
            setDisabler(false)
            setShow(true)
            const reviewList = Array.from(wordSet)
            setLista2(createList(topic, reviewList))
          }}><Text style={{ justifyContent: 'center', alignContent: 'center', fontSize: 16, color: 'white' }}>Review</Text>
        </Pressable>
      </View >
      <FlatList
        data={lista2}
        extraData={reviewState}
        renderItem={({ item, index }) => (
          <View style={{flexDirection: 'row', backgroundColor: '#000'}}>
            <Checkbox
              status={reviewList.includes(item)? 'checked': 'unchecked'}
              onPress={() => {
                if(!reviewList.includes(item)){
                  addReview(item)
                } else removeReview(item)
              }}
            ></Checkbox>
            <Text style={{color: '#fff'}}>{item}</Text>
          </View>
        )}>
      </FlatList>
    </View>
  );
}

