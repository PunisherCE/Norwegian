let testeIndex: number = -1
type wordsTo = { word: string, index: number }

function getWords(subject: subjectType): wordsTo {
  switch (subject) {
    case 'adjetives':
      currentIndex = Math.floor(Math.random() * adjetives.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: adjetives[currentIndex], index: currentIndex }

    case 'conjunctions':
      currentIndex = Math.floor(Math.random() * conjunctions.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: conjunctions[currentIndex], index: currentIndex }

    case 'adverbs':
      currentIndex = Math.floor(Math.random() * adverbs.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: adverbs[currentIndex], index: currentIndex }

    case 'other':
      currentIndex = Math.floor(Math.random() * other.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: other[currentIndex], index: currentIndex }

    case 'prepositions':
      currentIndex = Math.floor(Math.random() * prepositions.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: prepositions[currentIndex], index: currentIndex }

    case 'pronouns':
      currentIndex = Math.floor(Math.random() * pronouns.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: pronouns[currentIndex], index: currentIndex }

    case 'sustantives':
      currentIndex = Math.floor(Math.random() * sustantives.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: sustantives[currentIndex], index: currentIndex }

    case 'questions':
      currentIndex = Math.floor(Math.random() * questions.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: questions[currentIndex], index: currentIndex }

    case 'verbs':
      currentIndex = Math.floor(Math.random() * verbs.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: verbs[currentIndex], index: currentIndex }

    case 'various':
      currentIndex = Math.floor(Math.random() * various.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: various[currentIndex], index: currentIndex }

    case 'compilation':
      currentIndex = Math.floor(Math.random() * compilation.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: compilation[currentIndex], index: currentIndex }

    case 'many':
      currentIndex = Math.floor(Math.random() * many.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: many[currentIndex], index: currentIndex }

    case 'custom':
      currentIndex = Math.floor(Math.random() * custom.length)
      if (testeIndex !== currentIndex) {
        testeIndex = currentIndex
      } else return getWords(subject)

      return { word: custom[currentIndex], index: currentIndex }

    default:
      return { word: '', index: -1 }

  }
}

